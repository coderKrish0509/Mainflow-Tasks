# Create a list
my_list = [1, 2, 3]
print("Original List:", my_list)

# Add element
my_list.append(4)
print("After Adding 4:", my_list)


# Remove element
my_list.remove(4)
print("After Removing 4:", my_list)

# Modify element
my_list[0] = 10
print("After Modifying First Element:", my_list)


